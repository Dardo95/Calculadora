    private void leeNumero(String numero){
        
        String cadena1 = display.getText();
       if ( cadena1.equals("0")){
           cadena1 = numero;
       }
       
       else{
           cadena1 = cadena1 + numero;
       } 
       
       display.setText(cadena1);
       //miniDisplay.setText(cadena1);
        
    }
    
    private void leeOperador(String operador){
         String cadena1 = display.getText();
        operando1 = Double.valueOf(cadena1);
        operacion = operador;
        miniDisplay.setText(cadena1 + " " + (operador));
        display.setText ("0");
    }

