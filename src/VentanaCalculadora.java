    private void botonIgualMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonIgualMousePressed
        String cadena1 = display.getText();
        Double operando2 = Double.valueOf(cadena1);
        if (operacion.equals("+")){
            operando1 = operando1 + operando2;
        }
        if (operacion.equals("-")){
            operando1 = operando1 - operando2;
        }
        
        if (operacion.equals("*")){
            operando1 = operando1 * operando2;
        }
        
        if (operacion.equals("/")){
            operando1 = operando1 / operando2;
        }
        
        display.setText(String.valueOf(operando1));
        miniDisplay.setText("");
        
    }//GEN-LAST:event_botonIgualMousePressed

